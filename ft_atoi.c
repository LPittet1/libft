#include "libft.h"

int	ft_atoi(const char *nptr)
{
	long	i;
	long	res;

	i = 0;
	res = 0;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		i++;
	}
	while ('0' <= nptr[i] && nptr[i] <= '9')
	{
		res += (nptr[i] - 48);
		res *= 10;
		i++;
	}
	if (nptr[0] == '-')
		res *= -1;
	return (res / 10);
}
