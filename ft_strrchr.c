char	*ft_strrchr(char *s, int c)
{
	int	i;

	i = 0;
	while (s[i + 1])
		i++;
	while (i >= 0)
	{
		if (s[i] == c)
			return (&s[i]);
		i--;
	}
	return (0);
}
