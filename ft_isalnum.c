int	ft_isalnum(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (('0' <= str[i] && str[i] <= '9') || ('A' <= str[i] && str[i] <= 'Z')
			|| ('a' <= str[i] && str[i] <= 'z'))
		{
			i++;
		}
		else
			return (0);
	}
	return (1);
}
